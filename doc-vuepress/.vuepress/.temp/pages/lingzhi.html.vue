<ul>
<li>
<p><strong>原因</strong>：数据库字符集与IDE不一致</p>
</li>
<li>
<p><strong>解决</strong>：</p>
<ul>
<li>连接配置 → <code v-pre>Advanced</code> 标签页 → 添加参数：<br>
<code v-pre>useUnicode=true&amp;characterEncoding=UTF-8</code></li>
</ul>
</li>
</ul>
</blockquote>
<p><strong>3. 修改数据不生效</strong></p>
<blockquote>
<ul>
<li>
<p><strong>原因</strong>：未提交事务</p>
</li>
<li>
<p><strong>解决</strong>：</p>
<ol>
<li>
<p>开启手动事务：工具栏点击 <code v-pre>Auto-Commit</code> 切换为手动</p>
</li>
<li>
<p>执行修改后点击 <code v-pre>Submit</code></p>
</li>
<li>
<p>点击 <code v-pre>Commit</code> 提交事务</p>
</li>
</ol>
</li>
</ul>
</blockquote>
<p><strong>4. 插件卡顿</strong></p>
<blockquote>
<p><strong>解决</strong>：</p>
<p>关闭不用的数据库连接</p>
<p>限制查询结果行数：<code v-pre>Settings</code> → <code v-pre>Database Navigator</code> → <code v-pre>Max rows to show</code></p>
</blockquote>
