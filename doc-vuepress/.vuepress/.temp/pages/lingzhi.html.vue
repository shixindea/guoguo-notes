<p><strong>步骤详解</strong>：</p>
<p><strong>1.打开插件市场</strong></p>
<blockquote>
<ul>
<li>顶部菜单栏 → <code v-pre>File</code> → <code v-pre>Settings</code>（Windows/Linux）</li>
<li><em>或</em></li>
<li><code v-pre>IntelliJ IDEA</code> → <code v-pre>Preferences</code>（macOS）</li>
<li>左侧导航 → 选择 <code v-pre>Plugins</code> → 切换到 <code v-pre>Marketplace</code> 标签页。</li>
</ul>
</blockquote>
<p><img src="https://i-blog.csdnimg.cn/direct/65da1112cbd24473bf7856a67871c282.png" alt=""></p>
<p><strong>2.搜索插件</strong></p>
<blockquote>
<ul>
<li>在搜索框输入 <strong><code v-pre>Database Navigator</code></strong> → 按回车。</li>
<li><strong>认准官方标识</strong>：检查作者是否为 <code v-pre>JetBrains</code> 或 <code v-pre>RedNixon</code>（历史维护者），避免安装第三方仿冒插件。</li>
</ul>
</blockquote>
<p><img src="https://i-blog.csdnimg.cn/direct/acc782c228804bea910b6da2ed3c3392.png" alt=""></p>
<p><strong>3.安装并重启</strong></p>
<blockquote>
<ul>
<li>点击插件右侧的 <strong><code v-pre>Install</code></strong> 按钮 → 等待进度条完成。</li>
<li><strong>强制重启</strong>：安装完成后弹出提示 → 点击 <strong><code v-pre>Restart IDE</code></strong> 激活插件。</li>
<li><em>（未自动弹窗？手动重启IDEA即可）</em></li>
</ul>
</blockquote>
<p><img src="https://i-blog.csdnimg.cn/direct/6e436fcce7fc4f9f88c2848e749a58b9.png" alt=""></p>
<p><strong>4.验证安装</strong></p>
<blockquote>
<ul>
<li>重启后观察左侧工具栏 → 出现 <strong>数据库图标</strong>（<code v-pre>[ ]</code> 或 <code v-pre>&gt;_&gt;</code> 样式）即表示成功。</li>
<li>无图标？手动启用：<code v-pre>View</code> → <code v-pre>Tool Windows</code> → <code v-pre>Database Navigator</code>。</li>
</ul>
</blockquote>
