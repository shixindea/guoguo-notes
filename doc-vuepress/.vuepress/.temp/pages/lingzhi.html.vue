<h4 id="⚠️-避坑指南" tabindex="-1"><a class="header-anchor" href="#⚠️-避坑指南"><span><strong>⚠️ 避坑指南</strong></span></a></h4>
<p><strong>1.驱动报错？优先检查JDBC驱动！</strong></p>
<blockquote>
<p>连接数据库时若提示 <code v-pre>No suitable driver found</code>：</p>
<p>在配置数据源的界面 → 点击 <code v-pre>Driver</code> 选项卡 → 手动添加对应数据库的JDBC驱动包（如MySQL的 <code v-pre>mysql-connector-java-8.x.jar</code>）。</p>
<p><em>驱动包需提前下载到本地</em>。</p>
</blockquote>
<p><strong>2.重启后插件不显示？</strong></p>
<blockquote>
<p>进入 <code v-pre>Settings</code> → <code v-pre>Plugins</code> → <code v-pre>Installed</code> 标签页 → 确认 <code v-pre>Database Navigator</code> 已启用（复选框打钩✅）。</p>
</blockquote>
<p><strong>3.版本兼容性问题</strong></p>
<blockquote>
<p>手动安装时 → 务必下载与IDEA大版本匹配的插件（如IDEA 2023.x 选插件12.x版本）。</p>
</blockquote>
