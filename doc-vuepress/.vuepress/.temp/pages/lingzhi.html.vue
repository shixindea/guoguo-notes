<p><strong>操作流程</strong>：</p>
<p><strong>1.下载插件包</strong></p>
<blockquote>
<p>访问 <a href="https://plugins.jetbrains.com/plugin/1800-database-navigator" title="JetBrains 插件官网" target="_blank" rel="noopener noreferrer">JetBrains 插件官网</a></p>
<p>点击 <code v-pre>Download</code> → 选择 <strong>兼容当前IDEA版本</strong> 的 <code v-pre>.zip</code> 文件（如 <code v-pre>Database Navigator-12.x.zip</code>）。</p>
</blockquote>
<p><img src="https://i-blog.csdnimg.cn/direct/dd99fb79ed724caea45065a3620b0777.png" alt=""></p>
<p><strong>2.本地安装</strong></p>
<blockquote>
<p>打开IDE设置 → <code v-pre>Plugins</code> → 点击 ⚙️ 设置图标 → <code v-pre>Install Plugin from Disk...</code></p>
<p>选择下载的 <code v-pre>.zip</code> 文件 → 点击 <code v-pre>OK</code> 加载。</p>
</blockquote>
<p><img src="https://i-blog.csdnimg.cn/direct/fdba3efb328d4c3eb03aae0653a64c00.png" alt=""></p>
<p><strong>3.重启并生效</strong></p>
<blockquote>
